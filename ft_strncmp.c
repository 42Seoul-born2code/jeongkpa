#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned char	*p_s1;
	unsigned char	*p_s2;
	size_t			i;

	i = 0;
	p_s1 = (unsigned char *)s1;
	p_s2 = (unsigned char *)s2;
	while (i < n)
	{
		if (p_s1[i] == p_s2[i])
			i++;
		else
			return (p_s1[i] - p_s2[i]);
		if (!(p_s1[i]) && !(p_s2[i]))
			break ;
	}
	return (0);
}
